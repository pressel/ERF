constexpr amrex::Real kThermoTolFactor = amrex::Real(2.0e-5);
constexpr amrex::Real kStateTolFactor = amrex::Real(5.0e-5);
constexpr amrex::Real kAsymptoticRelTol = amrex::Real(2.0e-1);
