      -DMPI_CXX_LIBRARIES=$CRAY_MPICH_DIR/lib/libmpi.so \
      -DMPI_LIBRARY="$CRAY_MPICH_DIR/lib/libmpi.so" \
      -DCUDA_LIBRARY_DIRS=/opt/nvidia/hpc_sdk/Linux_x86_64/24.5/math_libs/12.4/targets/x86_64-linux/lib \
